"""Set is an unique collection of unique elements thet do not follow a specific order.
It can be modified, added, replaced or removed.
Elements in set are enclosed using curly braces."""

example_set = {'item1', 'item2', 'item3'}

example_set.add('item4')

# output: added object to set --> {'item1', 'item2', 'item3', 'item4'}

example_set.remove('item4')

#  outup: removed object from set --> {'item1', 'item2', 'item3'}
